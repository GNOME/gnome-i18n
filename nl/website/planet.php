	<p>Daarna kon ik eindelijk aan de slag. Ik bleek vóór morgenavond weer zo&#8217;n &#8220;technische&#8221; studie in elkaar te moeten gaan zetten. Zo eentje met heel veel tekst en weinig inhoud. Zucht. Geen goed begin. Er zit toch wel een enorm verschil tussen zelf je eigen creativiteit gebruiken, en dan gemotiveerd iets <em>moois</em> in elkaar zetten, en zuchtend van bovenop opgelegde opdrachten uitvoeren waarvan je het nut niet inziet. En dan nog die <em>verschrrrrikelijke</em> code die sommige van mijn collega&#8217;s in elkaar zetten. Jammer van die NDA, anders zou ik er hier op los pasten.<br />
Misschien moet ik eens gaan kijken of ik weer wat freelance kan doen. Dat was eigenlijk een stuk leuker dan dit. Ik zit bij Movial op uurbasis, kan dus altijd terugvallen.</p>
	<p>Rond een uur of vijf kwam mijn huisbaas langs om een nieuwe modem af te leveren. Eentje die niet elke 30 minuten de verbinding verbreekt, zoals de <a href="http://www.sagem.com/fr/produits/reseaux-telecom/modem-fast908.htm">SAGEM F@st™ 908</a>. Het is erg prettig om niet 16 keer per dag onder de tafel te hoeven kruipen om het stroomkabeltje uit de modem te rukken, 15 seconden te wachten, en hem weer terug te duwen.<br />
Huisbaas (tevens baas van internetafdeleing van <a href="http://www.elion.ee/">Elion</a>, de Estse KPN) vertelde dat ze 10k van die sagems hadden ingekocht, en er <em>toen</em> pas achterkwamen dat het <a href="http://www.google.com/search?hl=en&#038;lr=&#038;q=sagem+908&#038;btnG=Search">rommel</a> is. Nu proberen ze die modems zo snel mogelijk kwijt te raken, wat ze bij mij dus ook geprobeerd hebben. <em>Schurken</em>.
