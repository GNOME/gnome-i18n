#!/usr/bin/perl
#
#    allpo is a program that loads all translations from .desktop and
#    .directory files into .po file format and stores them back.
#    that makes translating .desktop files a lot faster and cleaner...
#
#    Probably there are still bugs in the code and some parts could
#    be written much more efficenlty, but for now it is enough that
#    it works for me
#    feel free to send comments to andraz.tori1@guest.arnes.si
