Se você quer ajudar, inscreva-se na <a
href="http://listas.cipsga.org.br/cgi-bin/mailman/listinfo/gnome-l10n-br">lista 
de discussões do GNOME-l10n-BR</a>
e envie um email de apresentação, dizendo em que pretende ajudar.
Leia também o nosso <a href="ajudar.php">pequeno manual sobre como
ajudar</a>.
<p>
Procure sempre manter o coordenador informado das suas atividades.
Isso garante o bom andamento do processo de tradução e evita esforço
duplicado. Informe a lista também, quando necessário.
